'''
The integer expressions of some of the most common intervallic structures.

Since every interval structure is stored as a unique integer, it is very easy
to relate modal transformations back to their canonical centres. This allows us
to recognize that a scale must be the nth mode of a canonical scale, or that a 
chord must be the nth inversion, drop 2 of a canonical chord.
'''
from src.data import keywords

# Intervals of the 12 tone octave.
UNISON = 0b1
HEMITONE = 0b11
TONE = 0b101
HEMIOLION = 0b1001
DITONE = 0b10001
DIATESSARON = 0b100001
TRITONE = 0b1000001
DIAPENTE = 0b10000001
COMPOUND_HEMITONE = 0b100000001
COMPOUND_TONE = 0b1000000001
COMPOUND_HEMIOLION = 0b10000000001
COMPOUND_DITONE = 0b100000000001
DIAPASON = 0b1000000000001

# COMMON TRIADS
# -------------
# Structures that are compounds of 2 intervals.
MAJOR_CH = DITONE | DIAPENTE
MINOR_CH = HEMIOLION | DIAPENTE
MINOR_FLAT_5_CH = HEMIOLION | TRITONE
MAJOR_SHARP_5_CH = DITONE | COMPOUND_HEMITONE
# minor #5 is an inversion of major triad, not used as a separate chord.
MAJOR_FLAT_5_CH = DITONE | TRITONE
SUS2_CH = TONE | DIAPENTE
# sus2 and sus4 are inversions of each other,
# but we want to try to recognize them as separate chords,
# depending on the bass.
SUS4_CH = DIATESSARON | DIAPENTE

TRIADS_KEY_TO_VALUE_MAP = {
    keywords.MAJOR_TRIAD: MAJOR_CH,
    keywords.MINOR_TRIAD: MINOR_CH,
    keywords.MINOR_FLAT_5: MINOR_FLAT_5_CH,
    keywords.MAJOR_FLAT_5: MAJOR_FLAT_5_CH,
    keywords.MAJOR_SHARP_5: MAJOR_SHARP_5_CH,
    keywords.SUS2_TRIAD: SUS2_CH,
    keywords.SUS4_TRIAD: SUS4_CH
}

# COMMON TETRADS
# --------------
# Structures that are compounds of three intervals.
MAJOR_SEVENTH_CH = MAJOR_CH | COMPOUND_DITONE
MINOR_SEVENTH_CH = MINOR_CH | COMPOUND_HEMIOLION
MAJOR_SIXTH_CH = MAJOR_CH | COMPOUND_TONE
MINOR_SIXTH_CH = MINOR_CH | COMPOUND_TONE
MINOR_MAJOR_SEVENTH_CH = MINOR_CH | COMPOUND_DITONE
DOMINANT_SEVENTH_CH = MAJOR_CH | COMPOUND_HEMIOLION
MINOR_SEVEN_FLAT_FIVE_CH = MINOR_FLAT_5_CH | COMPOUND_HEMIOLION
DIMINISHED_SEVENTH_CH = MINOR_FLAT_5_CH | COMPOUND_TONE
AUGMENTED_SEVENTH_CH = MAJOR_SHARP_5_CH | COMPOUND_HEMIOLION
# augmented triad + major 6 is an inversion of minmaj7
AUGMENTED_MAJOR_SEVENTH_CH = MAJOR_SHARP_5_CH | COMPOUND_DITONE
DOMINANT_SEVENTH_FLAT_FIVE_CH = MAJOR_FLAT_5_CH | COMPOUND_HEMIOLION

TETRADS_KEY_TO_VALUE_MAP = {
    keywords.MAJOR_SEVENTH: MAJOR_SEVENTH_CH,
    keywords.MINOR_SEVENTH: MINOR_SEVENTH_CH,
    keywords.MAJOR_SIXTH: MAJOR_SIXTH_CH,
    keywords.MINOR_SIXTH: MINOR_SIXTH_CH,
    keywords.MINOR_MAJOR_SEVENTH: MINOR_MAJOR_SEVENTH_CH,
    keywords.DOMINANT_SEVENTH: DOMINANT_SEVENTH_CH,
    keywords.DOMINANT_SEVENTH_FLAT_FIVE: DOMINANT_SEVENTH_FLAT_FIVE_CH,
    keywords.DIMINISHED_SEVENTH: DIMINISHED_SEVENTH_CH,
    keywords.AUGMENTED_MAJOR_SEVENTH: AUGMENTED_MAJOR_SEVENTH_CH,
    keywords.AUGMENTED_SEVENTH: AUGMENTED_SEVENTH_CH,
    keywords.MINOR_SEVEN_FLAT_FIVE: MINOR_SEVEN_FLAT_FIVE_CH
}

# HEPTATONIC SCALES
# -----------------
DIATONIC_SCALE = TONE | DITONE | DIATESSARON | DIAPENTE | COMPOUND_TONE | COMPOUND_DITONE
ALTERED_SCALE = HEMITONE | HEMIOLION | DITONE | TRITONE | COMPOUND_HEMITONE | COMPOUND_HEMIOLION
HEMITONIC_SCALE = HEMITONE | DITONE | DIATESSARON | DIAPENTE | COMPOUND_TONE | COMPOUND_DITONE
HEMIOLIC_SCALE = HEMIOLION | DITONE | DIATESSARON | DIAPENTE | COMPOUND_TONE | COMPOUND_DITONE
DIMINISHED_SCALE = TONE | DITONE | DIATESSARON | TRITONE | COMPOUND_TONE | COMPOUND_DITONE
AUGMENTED_SCALE = TONE | DITONE | DIATESSARON | COMPOUND_HEMITONE | COMPOUND_TONE | COMPOUND_DITONE
HARMONIC_SCALE = TONE | DITONE | DIATESSARON | DIAPENTE | COMPOUND_HEMITONE | COMPOUND_DITONE
BISEPTIMAL_SCALE = TONE | DITONE | DIATESSARON | DIAPENTE | COMPOUND_HEMIOLION | COMPOUND_DITONE
PALEOCHROMATIC_SCALE = HEMITONE | DITONE | DIATESSARON | TRITONE | COMPOUND_TONE | COMPOUND_DITONE

# The canonical order in which heptatonic scales will searched.
HEPTATONIC_ORDER_VALUES = (
    DIATONIC_SCALE,
    ALTERED_SCALE,
    HEMITONIC_SCALE,
    HEMIOLIC_SCALE,
    DIMINISHED_SCALE,
    AUGMENTED_SCALE,
    HARMONIC_SCALE,
    BISEPTIMAL_SCALE,
    PALEOCHROMATIC_SCALE
)
HEPTATONIC_ORDER_VALUE_TO_KEY_MAP = dict(
    zip(HEPTATONIC_ORDER_VALUES, keywords.HEPTATONIC_ORDER_KEYS))
HEPTATONIC_ORDER_KEY_TO_VALUE_MAP = dict(
    zip(keywords.HEPTATONIC_ORDER_KEYS, HEPTATONIC_ORDER_VALUES))
