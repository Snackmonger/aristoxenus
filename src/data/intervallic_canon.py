'''
The integer expressions of some of the most common intervallic structures.

Since every interval structure is stored as a unique integer, it is very easy
to relate modal transformations back to their canonical centres. This allows us
to recognize that a scale must be the nth mode of a canonical scale, or that a 
chord must be the nth inversion, drop 2 of a canonical chord.
'''
from src.data import keywords

# Intervals of the 12 tone octave.
UNISON_INTV = 0b1
HEMITONE_INTV = 0b11
TONE_INTV = 0b101
HEMIOLION_INTV = 0b1001
DITONE_INTV = 0b10001
DIATESSARON_INTV = 0b100001
TRITONE_INTV = 0b1000001
DIAPENTE_INTV = 0b10000001
COMPOUND_HEMITONE_INTV = 0b100000001
COMPOUND_TONE_INTV = 0b1000000001
COMPOUND_HEMIOLION_INTV = 0b10000000001
COMPOUND_DITONE_INTV = 0b100000000001
DIAPASON_INTV = 0b1000000000001

# COMMON TRIADS
# -------------
# Structures that are compounds of 2 intervals.
MAJOR_CH = DITONE_INTV | DIAPENTE_INTV
MINOR_CH = HEMIOLION_INTV | DIAPENTE_INTV
MINOR_FLAT_5_CH = HEMIOLION_INTV | TRITONE_INTV
MAJOR_SHARP_5_CH = DITONE_INTV | COMPOUND_HEMITONE_INTV
# minor #5 is an inversion of major triad, not used as a separate chord.
MAJOR_FLAT_5_CH = DITONE_INTV | TRITONE_INTV
SUS2_CH = TONE_INTV | DIAPENTE_INTV
# sus2 and sus4 are inversions of each other,
# but we want to try to recognize them as separate chords,
# depending on the bass.
SUS4_CH = DIATESSARON_INTV | DIAPENTE_INTV

TRIADS_KEY_TO_VALUE_MAP = {
    keywords.MAJOR_TRIAD: MAJOR_CH,
    keywords.MINOR_TRIAD: MINOR_CH,
    keywords.MINOR_FLAT_5: MINOR_FLAT_5_CH,
    keywords.MAJOR_FLAT_5: MAJOR_FLAT_5_CH,
    keywords.MAJOR_SHARP_5: MAJOR_SHARP_5_CH,
    keywords.SUS2_TRIAD: SUS2_CH,
    keywords.SUS4_TRIAD: SUS4_CH
}

# COMMON TETRADS
# --------------
# Structures that are compounds of three intervals.
MAJOR_SEVENTH_CH = MAJOR_CH | COMPOUND_DITONE_INTV
MINOR_SEVENTH_CH = MINOR_CH | COMPOUND_HEMIOLION_INTV
MAJOR_SIXTH_CH = MAJOR_CH | COMPOUND_TONE_INTV
MINOR_SIXTH_CH = MINOR_CH | COMPOUND_TONE_INTV
MINOR_MAJOR_SEVENTH_CH = MINOR_CH | COMPOUND_DITONE_INTV
DOMINANT_SEVENTH_CH = MAJOR_CH | COMPOUND_HEMIOLION_INTV
MINOR_SEVEN_FLAT_FIVE_CH = MINOR_FLAT_5_CH | COMPOUND_HEMIOLION_INTV
DIMINISHED_SEVENTH_CH = MINOR_FLAT_5_CH | COMPOUND_TONE_INTV
AUGMENTED_SEVENTH_CH = MAJOR_SHARP_5_CH | COMPOUND_HEMIOLION_INTV
# augmented triad + major 6 is an inversion of minmaj7
AUGMENTED_MAJOR_SEVENTH_CH = MAJOR_SHARP_5_CH | COMPOUND_DITONE_INTV
DOMINANT_SEVENTH_FLAT_FIVE_CH = MAJOR_FLAT_5_CH | COMPOUND_HEMIOLION_INTV

TETRADS_KEY_TO_VALUE_MAP = {
    keywords.MAJOR_SEVENTH: MAJOR_SEVENTH_CH,
    keywords.MINOR_SEVENTH: MINOR_SEVENTH_CH,
    keywords.MAJOR_SIXTH: MAJOR_SIXTH_CH,
    keywords.MINOR_SIXTH: MINOR_SIXTH_CH,
    keywords.MINOR_MAJOR_SEVENTH: MINOR_MAJOR_SEVENTH_CH,
    keywords.DOMINANT_SEVENTH: DOMINANT_SEVENTH_CH,
    keywords.DOMINANT_SEVENTH_FLAT_FIVE: DOMINANT_SEVENTH_FLAT_FIVE_CH,
    keywords.DIMINISHED_SEVENTH: DIMINISHED_SEVENTH_CH,
    keywords.AUGMENTED_MAJOR_SEVENTH: AUGMENTED_MAJOR_SEVENTH_CH,
    keywords.AUGMENTED_SEVENTH: AUGMENTED_SEVENTH_CH,
    keywords.MINOR_SEVEN_FLAT_FIVE: MINOR_SEVEN_FLAT_FIVE_CH
}

# HEPTATONIC SCALES
# -----------------
DIATONIC_SCALE = TONE_INTV | DITONE_INTV | DIATESSARON_INTV | DIAPENTE_INTV | COMPOUND_TONE_INTV | COMPOUND_DITONE_INTV
ALTERED_SCALE = HEMITONE_INTV | HEMIOLION_INTV | DITONE_INTV | TRITONE_INTV | COMPOUND_HEMITONE_INTV | COMPOUND_HEMIOLION_INTV
HEMITONIC_SCALE = HEMITONE_INTV | DITONE_INTV | DIATESSARON_INTV | DIAPENTE_INTV | COMPOUND_TONE_INTV | COMPOUND_DITONE_INTV
HEMIOLIC_SCALE = HEMIOLION_INTV | DITONE_INTV | DIATESSARON_INTV | DIAPENTE_INTV | COMPOUND_TONE_INTV | COMPOUND_DITONE_INTV
DIMINISHED_SCALE = TONE_INTV | DITONE_INTV | DIATESSARON_INTV | TRITONE_INTV | COMPOUND_TONE_INTV | COMPOUND_DITONE_INTV
AUGMENTED_SCALE = TONE_INTV | DITONE_INTV | DIATESSARON_INTV | COMPOUND_HEMITONE_INTV | COMPOUND_TONE_INTV | COMPOUND_DITONE_INTV
HARMONIC_SCALE = TONE_INTV | DITONE_INTV | DIATESSARON_INTV | DIAPENTE_INTV | COMPOUND_HEMITONE_INTV | COMPOUND_DITONE_INTV
BISEPTIMAL_SCALE = TONE_INTV | DITONE_INTV | DIATESSARON_INTV | DIAPENTE_INTV | COMPOUND_HEMIOLION_INTV | COMPOUND_DITONE_INTV
PALEOCHROMATIC_SCALE = HEMITONE_INTV | DITONE_INTV | DIATESSARON_INTV | TRITONE_INTV | COMPOUND_TONE_INTV | COMPOUND_DITONE_INTV


# The canonical order in which heptatonic scales will searched.
HEPTATONIC_ORDER_VALUES = (
    DIATONIC_SCALE,
    ALTERED_SCALE,
    HEMITONIC_SCALE,
    HEMIOLIC_SCALE,
    DIMINISHED_SCALE,
    AUGMENTED_SCALE,
    HARMONIC_SCALE,
    BISEPTIMAL_SCALE,
    PALEOCHROMATIC_SCALE
)
HEPTATONIC_ORDER_VALUE_TO_KEY_MAP = dict(
    zip(HEPTATONIC_ORDER_VALUES, keywords.HEPTATONIC_ORDER_KEYS))
HEPTATONIC_ORDER_KEY_TO_VALUE_MAP = dict(
    zip(keywords.HEPTATONIC_ORDER_KEYS, HEPTATONIC_ORDER_VALUES))
